import { isOdd, isEven } from '../../src'

describe('isOdd/isEven', () => {
  it('isOdd', () => {
    expect(isOdd(0)).toBe(false)
    expect(isOdd(1)).toBe(true)
    expect(isOdd(-1)).toBe(true)
    expect(isOdd(-2)).toBe(false)
    expect(isOdd(3)).toBe(true)
    expect(isOdd(4)).toBe(false)
    expect(isOdd(97656788765674)).toBe(false)
    expect(isOdd(97656788765677)).toBe(true)
    expect(isOdd(-1121321213123121)).toBe(true)
    expect(isOdd(-1121321213123122)).toBe(false)
    // bigint 测试
    expect(isOdd(BigInt(0))).toBe(false)
    expect(isOdd(BigInt(1))).toBe(true)
    expect(isOdd(BigInt(-1))).toBe(true)
    expect(isOdd(BigInt(-2))).toBe(false)
    expect(isOdd(BigInt(3))).toBe(true)
    expect(isOdd(BigInt(4))).toBe(false)
    expect(isOdd(BigInt(97656788765674))).toBe(false)
    expect(isOdd(BigInt(97656788765677))).toBe(true)
    expect(isOdd(BigInt(-1121321213123121))).toBe(true)
    expect(isOdd(BigInt(-1121321213123122))).toBe(false)
  })
  it('isEven', () => {
    expect(isEven(0)).toBe(true)
    expect(isEven(1)).toBe(false)
    expect(isEven(-1)).toBe(false)
    expect(isEven(-2)).toBe(true)
    expect(isEven(3)).toBe(false)
    expect(isEven(4)).toBe(true)
    expect(isEven(97656788765674)).toBe(true)
    expect(isEven(97656788765677)).toBe(false)
    expect(isEven(-1121321213123121)).toBe(false)
    expect(isEven(-1121321213123122)).toBe(true)
    // bigint 测试
    expect(isEven(BigInt(0))).toBe(true)
    expect(isEven(BigInt(1))).toBe(false)
    expect(isEven(BigInt(-1))).toBe(false)
    expect(isEven(BigInt(-2))).toBe(true)
    expect(isEven(BigInt(3))).toBe(false)
    expect(isEven(BigInt(4))).toBe(true)
    expect(isEven(BigInt(97656788765674))).toBe(true)
    expect(isEven(BigInt(97656788765677))).toBe(false)
    expect(isEven(BigInt(-1121321213123121))).toBe(false)
    expect(isEven(BigInt(-1121321213123122))).toBe(true)
  })
})
